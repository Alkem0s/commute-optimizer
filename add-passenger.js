function updateProgressBar(current, total) {
  console.log(`üìä Progress g√ºncelleniyor: ${current}/${total}`);
  
  const progressBar = document.getElementById('progress-bar');
  const progressText = document.getElementById('progress-text');
  
  if (progressBar && progressText) {
    const percentage = total > 0 ? (current / total) * 100 : 0;
    progressBar.style.width = percentage + '%';
    progressText.textContent = `${current} / ${total} yolcu i≈ülendi`;
    console.log(`‚úÖ Progress g√ºncellendi: ${percentage.toFixed(1)}%`);
  } else {
    console.warn('‚ö†Ô∏è Progress elementleri bulunamadƒ±:', { 
      progressBar: !!progressBar, 
      progressText: !!progressText 
    });
  }
}

